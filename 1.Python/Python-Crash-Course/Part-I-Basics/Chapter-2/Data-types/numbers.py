# Numbers - anything in the numerical system. Different types of numbers exist in python such as integers, floats...

# Integers - Negative and Positive whole numbers

integer_variable = 15 # An integer
another_integer = 5 # An integer
print(integer_variable)
print(another_integer)

# Operations are possible on integers
sum = integer_variable + another_integer
print(sum)
difference = integer_variable - another_integer
print(difference)
product = integer_variable * another_integer
print(product)
quotient = integer_variable / another_integer
print(quotient)
exponential = integer_variable ** another_integer
print(exponential)

# Floats - Negative and Positive decimal numbers
float_variable = 3.5 # A float
another_float = 0.5 # A float

# Operations are possible on floats just as integers
sum = float_variable + another_float
print(sum)
difference = float_variable - another_float
print(difference)
product = float_variable * another_float
print(product)
quotient = float_variable / another_float
print(quotient)
exponential = float_variable ** another_float
print(exponential)

