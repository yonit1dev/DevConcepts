// Expression, Statements...

// Bindings or in other words variables.
let caught = 5 * 5;

console.log(caught);

let mood = "light";
console.log(mood);

mood = "dark";
console.log(mood);

let one = 1, two = 2; // Two variable assignments with one let keyword
console.log(one + two);

let three; // Undefined
console.log(three);

// Using var and const
var name = "Ayda";
const greeting = "Hello, ";

console.log(greeting + name);